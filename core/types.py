@dataclass(frozen=True)
class Signal:
    """
    Trading signal generated by a strategy.

    Immutable to ensure signal integrity through the processing pipeline.
    """
    symbol: str
    action: SignalAction
    confidence: float  # 0.0 - 1.0
    strength: float    # 0.0 - 1.0, signal magnitude
    entry_price: float
    signal_id: str = ""  # Unique identifier for tracking
    stop_loss: Optional[float] = None
    take_profit: Optional[float] = None
    strategy_name: str = ""
    reasoning: str = ""
    regime: MarketRegime = MarketRegime.UNKNOWN
    timestamp: datetime = field(default_factory=datetime.utcnow)
    metadata: Dict[str, Any] = field(default_factory=dict)

    def __post_init__(self) -> None:
        # Validate confidence and strength are in valid range
        object.__setattr__(self, 'confidence', max(0.0, min(1.0, self.confidence)))
        object.__setattr__(self, 'strength', max(0.0, min(1.0, self.strength)))

    @property
    def is_entry(self) -> bool:
        """Check if this is an entry signal (BUY or SELL)."""
        return self.action in (SignalAction.BUY, SignalAction.SELL)

    @property
    def is_exit(self) -> bool:
        """Check if this is an exit signal (CLOSE)."""
        return self.action == SignalAction.CLOSE

    @property
    def score(self) -> float:
        """Combined confidence * strength score for ranking."""
        return self.confidence * self.strength

    def to_dict(self) -> Dict[str, Any]:
        """Serialize to dictionary."""
        return {
            "symbol": self.symbol,
            "action": self.action.value,
            "confidence": self.confidence,
            "strength": self.strength,
            "entry_price": self.entry_price,
            "stop_loss": self.stop_loss,
            "take_profit": self.take_profit,
            "strategy_name": self.strategy_name,
            "reasoning": self.reasoning,
            "regime": self.regime.value,
            "timestamp": self.timestamp.isoformat(),
            "metadata": self.metadata,
        }


@dataclass
class Position:
    """
    Current position state.

    Mutable to track real-time position updates.
    """
    symbol: str
    side: Side
    quantity: float
    entry_price: float
    entry_time: datetime
    current_price: float = 0.0
    stop_loss: Optional[float] = None
    take_profit: Optional[float] = None
    strategy_name: str = ""
    position_id: str = ""

    @property
    def notional_value(self) -> float:
        """Current position value."""
        return self.quantity * self.current_price

    @property
    def entry_value(self) -> float:
        """Original position value."""
        return self.quantity * self.entry_price

    @property
    def unrealized_pnl(self) -> float:
        """Unrealized P&L in quote currency."""
        if self.side == Side.BUY:
            return (self.current_price - self.entry_price) * self.quantity
        else:
            return (self.entry_price - self.current_price) * self.quantity

    @property
    def unrealized_pnl_pct(self) -> float:
        """Unrealized P&L as percentage."""
        if self.entry_value == 0:
            return 0.0
        return (self.unrealized_pnl / self.entry_value) * 100.0

    @property
    def is_profitable(self) -> bool:
        """Check if position is currently profitable."""
        return self.unrealized_pnl > 0

    def should_stop_out(self) -> bool:
        """Check if stop loss has been triggered."""
        if self.stop_loss is None:
            return False
        if self.side == Side.BUY:
            return self.current_price <= self.stop_loss
        else:
            return self.current_price >= self.stop_loss

    def should_take_profit(self) -> bool:
        """Check if take profit has been triggered."""
        if self.take_profit is None:
            return False
        if self.side == Side.BUY:
            return self.current_price >= self.take_profit
        else:
            return self.current_price <= self.take_profit


@dataclass(frozen=True)
class OrderRequest:
    """
    Order request to be submitted to an exchange.

    Immutable to ensure order parameters don't change during processing.
    """
    symbol: str
    side: Side
    order_type: OrderType
    quantity: float
    price: Optional[float] = None  # For limit orders
    stop_price: Optional[float] = None  # For stop orders
    client_order_id: str = ""
    time_in_force: str = "GTC"  # GTC, IOC, FOK
    reduce_only: bool = False
    post_only: bool = False
    execution_algo: ExecutionAlgo = ExecutionAlgo.MARKET
    algo_params: Dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> Dict[str, Any]:
        """Serialize to dictionary for exchange submission."""
        return {
            "symbol": self.symbol,
            "side": self.side.value,
            "type": self.order_type.value,
            "quantity": self.quantity,
            "price": self.price,
            "stop_price": self.stop_price,
            "client_order_id": self.client_order_id,
            "time_in_force": self.time_in_force,
            "reduce_only": self.reduce_only,
            "post_only": self.post_only,
        }


@dataclass
class OrderResult:
    """
    Result of an order execution.
    """
    order_id: str
    client_order_id: str
    symbol: str
    side: Side
    order_type: OrderType
    status: OrderStatus
    requested_quantity: float
    filled_quantity: float
    remaining_quantity: float
    average_price: Optional[float]
    fees: float = 0.0
    fee_currency: str = ""
    timestamp: datetime = field(default_factory=datetime.utcnow)
    exchange: str = ""
    raw_response: Dict[str, Any] = field(default_factory=dict)
    error_message: str = ""

    @property
    def is_filled(self) -> bool:
        """Check if order is fully filled."""
        return self.status == OrderStatus.FILLED

    @property
    def is_partial(self) -> bool:
        """Check if order is partially filled."""
        return self.status == OrderStatus.PARTIALLY_FILLED

    @property
    def fill_rate(self) -> float:
        """Percentage of order filled."""
        if self.requested_quantity == 0:
            return 0.0
        return (self.filled_quantity / self.requested_quantity) * 100.0


