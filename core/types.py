@dataclass(frozen=True)
class Ticker:
    """
    Real-time ticker data.
    """
    symbol: str
    bid: float
    ask: float
    last: float
    volume_24h: float
    timestamp: datetime
    exchange: str

    @property
    def mid(self) -> float:
        """Mid-market price."""
        return (self.bid + self.ask) / 2.0

    @property
    def spread(self) -> float:
        """Bid-ask spread in quote currency."""
        return self.ask - self.bid

    @property
    def spread_bps(self) -> float:
        """Bid-ask spread in basis points."""
        if self.mid == 0:
            return 0.0
        return (self.spread / self.mid) * 10000.0


