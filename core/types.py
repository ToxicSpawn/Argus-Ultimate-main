@dataclass(frozen=True)
class RegimeSnapshot:
    """
    Market regime detection result.
    """
    regime: MarketRegime
    trend_score: float  # -1 (bearish) to 1 (bullish)
    volatility: float   # Annualized volatility
    volatility_percentile: float  # 0-100
    sentiment_score: float  # -1 (fear) to 1 (greed)
    confidence: float  # Regime detection confidence
    timestamp: datetime = field(default_factory=datetime.utcnow)

    @property
    def is_trending(self) -> bool:
        """Check if market is trending."""
        return self.regime in (MarketRegime.TREND_UP, MarketRegime.TREND_DOWN)

    @property
    def is_ranging(self) -> bool:
        """Check if market is ranging."""
        return self.regime == MarketRegime.RANGE

    @property
    def is_volatile(self) -> bool:
        """Check if market is highly volatile."""
        return self.regime == MarketRegime.HIGH_VOL


