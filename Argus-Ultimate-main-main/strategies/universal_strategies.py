class StrategyRegistry:
    """Registry of all available trading strategies."""
    
    def __init__(self):
        self.strategies: Dict[str, TradingStrategy] = {}
        self._register_all()
    
    def _register_all(self) -> None:
        """Register all strategies."""
        # Trend Following
        self.register(SMACrossover(10, 30))
        self.register(SMACrossover(5, 20))
        self.register(EMACrossover(12, 26))
        self.register(ADXTrendStrength(14, 25))
        self.register(ParabolicSAR())
        
        # Momentum
        self.register(RSIMomentum(14, 70, 30))
        self.register(RSIMomentum(9, 75, 25))
        self.register(MACDMomentum(12, 26, 9))
        self.register(ROCMomentum(10, 0.02))
        self.register(StochasticMomentum(14, 3))
        
        # Mean Reversion
        self.register(BollingerBandReversion(20, 2.0))
        self.register(BollingerBandReversion(10, 1.5))
        self.register(ZScoreReversion(20, 2.0, 0.5))
        self.register(RSIReversion(14))
        
        # Breakout
        self.register(RangeBreakout(20, 0.005))
        self.register(RangeBreakout(50, 0.003))
        self.register(VolumeBreakout(20, 1.5))
        
        # Scalping
        self.register(SpreadScalping(0.001))
        self.register(OrderFlowScalping(1.5))
        
        # Statistical Arbitrage
        self.register(PairsTrading(2.0, 0.0))
        self.register(CointegrationReversion(50, 2.0))
        
        # Market Making
        self.register(SimpleMarketMaking(0.001))
        
        # Volume Profile
        self.register(VolumeProfileTrading(100, 20))
    
    def register(self, strategy: TradingStrategy) -> None:
        """Register a strategy."""
        self.strategies[strategy.name] = strategy
    
    def get_all_signals(self, prices: List[float], **kwargs) -> List[Signal]:
        """Get signals from all strategies."""
        signals = []
        
        for strategy in self.strategies.values():
            if not strategy.enabled:
                continue
            
            signal = strategy.analyze(prices, **kwargs)
            if signal:
                signals.append(signal)
                strategy.signals_generated += 1
        
        return signals
    
    def get_best_strategy(self) -> Optional[TradingStrategy]:
        """Get the best performing strategy."""
        if not self.strategies:
            return None
        
        best = max(self.strategies.values(), key=lambda s: s.get_score())
        return best
    
    def get_strategy_stats(self) -> Dict[str, Dict[str, Any]]:
        """Get stats for all strategies."""
        stats = {}
        for name, strategy in self.strategies.items():
            stats[name] = {
                "enabled": strategy.enabled,
                "signals": strategy.signals_generated,
                "win_rate": strategy.get_win_rate(),
                "total_pnl": strategy.total_pnl,
                "score": strategy.get_score(),
            }
        return stats


# Singleton
_registry: Optional[StrategyRegistry] = None


def get_strategy_registry() -> StrategyRegistry:
    """Get or create singleton registry."""
    global _registry
    if _registry is None:
        _registry = StrategyRegistry()
    return _registry


def reset_registry() -> None:
    """Reset singleton."""
    global _registry
    _registry = None
