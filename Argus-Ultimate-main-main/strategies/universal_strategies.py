# BASE STRATEGY CLASS
# ============================================================================

@dataclass
class Signal:
    """Trading signal generated by a strategy."""
    action: str  # buy, sell, hold
    confidence: float  # 0-1
    strategy: str
    reason: str
    strength: float  # How strong the signal is
    metadata: Dict[str, Any] = field(default_factory=dict)


class TradingStrategy(ABC):
    """Base class for all trading strategies."""
    
    def __init__(self, name: str, timeframe: str = "1m"):
        self.name = name
        self.timeframe = timeframe
        self.enabled = True
        self.weight: float = 1.0  # For ensemble weighting
        
        # Performance tracking
        self.signals_generated: int = 0
        self.winning_signals: int = 0
        self.total_pnl: float = 0.0
        self.recent_performance: Deque[float] = deque(maxlen=100)
    
    @abstractmethod
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        """Analyze prices and generate signal."""
        pass
    
    def record_outcome(self, pnl: float) -> None:
        """Record outcome of a trade based on this strategy's signal."""
        self.total_pnl += pnl
        self.recent_performance.append(pnl)
        if pnl > 0:
            self.winning_signals += 1
    
    def get_win_rate(self) -> float:
        """Get win rate."""
        if self.signals_generated == 0:
            return 0.5
        return self.winning_signals / self.signals_generated
    
    def get_score(self) -> float:
        """Get strategy score for weighting."""
        if len(self.recent_performance) < 10:
            return 0.5
        
        recent = list(self.recent_performance)
        mean_pnl = np.mean(recent)
        std_pnl = np.std(recent) if np.std(recent) > 0 else 0.001
        
        # Sharpe-like score
        return max(0.1, 0.5 + mean_pnl / std_pnl * 0.1)


