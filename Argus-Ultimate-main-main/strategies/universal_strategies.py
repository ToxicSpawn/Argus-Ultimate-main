class SimpleMarketMaking(TradingStrategy):
    """Simple market making around fair value."""
    
    def __init__(self, spread_pct: float = 0.001):
        super().__init__(f"MarketMaking")
        self.spread_pct = spread_pct
        self.fair_value: float = 0.0
        self.inventory: float = 0.0
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        if len(prices) < 10:
            return None
        
        # Update fair value (EMA)
        self.fair_value = np.mean(prices[-10:])
        
        current = prices[-1]
        deviation = (current - self.fair_value) / self.fair_value
        
        # Skew quotes based on inventory
        inventory_skew = -self.inventory * 0.0001
        
        # Buy when price is below fair value + skew
        if deviation < -self.spread_pct + inventory_skew:
            return Signal(
                action="buy",
                confidence=0.5,
                strategy=self.name,
                reason=f"Market making buy (dev={deviation:.3%})",
                strength=abs(deviation) / self.spread_pct,
            )
        
        # Sell when price is above fair value + skew
        if deviation > self.spread_pct + inventory_skew:
            return Signal(
                action="sell",
                confidence=0.5,
                strategy=self.name,
                reason=f"Market making sell (dev={deviation:.3%})",
                strength=abs(deviation) / self.spread_pct,
            )
        
        return None


