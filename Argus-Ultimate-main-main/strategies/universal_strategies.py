class RangeBreakout(TradingStrategy):
    """Breakout from consolidation range."""
    
    def __init__(self, lookback: int = 20, breakout_pct: float = 0.005):
        super().__init__(f"RangeBreakout_{lookback}")
        self.lookback = lookback
        self.breakout_pct = breakout_pct
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        if len(prices) < self.lookback:
            return None
        
        # Calculate range
        highest = max(prices[-self.lookback:])
        lowest = min(prices[-self.lookback:])
        range_size = highest - lowest
        
        if range_size == 0:
            return None
        
        current = prices[-1]
        
        # Breakout above
        if current > highest * (1 + self.breakout_pct):
            strength = (current - highest) / range_size
            return Signal(
                action="buy",
                confidence=min(0.85, 0.6 + strength),
                strategy=self.name,
                reason=f"Bullish breakout above {self.lookback}-period high",
                strength=strength,
            )
        
        # Breakdown below
        if current < lowest * (1 - self.breakout_pct):
            strength = (lowest - current) / range_size
            return Signal(
                action="sell",
                confidence=min(0.85, 0.6 + strength),
                strategy=self.name,
                reason=f"Bearish breakdown below {self.lookback}-period low",
                strength=strength,
            )
        
        return None


class VolumeBreakout(TradingStrategy):
    """Breakout confirmed by volume."""
    
    def __init__(self, lookback: int = 20, volume_mult: float = 1.5):
        super().__init__(f"VolumeBreakout_{lookback}")
        self.lookback = lookback
        self.volume_mult = volume_mult
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        volumes = kwargs.get("volumes", [])
        
        if len(prices) < self.lookback or len(volumes) < self.lookback:
            return None
        
        highest = max(prices[-self.lookback:])
        lowest = min(prices[-self.lookback:])
        avg_volume = np.mean(volumes[-self.lookback:])
        current_volume = volumes[-1]
        
        # Volume spike + price breakout
        if current_volume > avg_volume * self.volume_mult:
            if prices[-1] > highest:
                return Signal(
                    action="buy",
                    confidence=0.75,
                    strategy=self.name,
                    reason=f"Volume breakout (vol={current_volume/avg_volume:.1f}x avg)",
                    strength=current_volume / avg_volume / self.volume_mult,
                )
            elif prices[-1] < lowest:
                return Signal(
                    action="sell",
                    confidence=0.75,
                    strategy=self.name,
                    reason=f"Volume breakdown (vol={current_volume/avg_volume:.1f}x avg)",
                    strength=current_volume / avg_volume / self.volume_mult,
                )
        
        return None


