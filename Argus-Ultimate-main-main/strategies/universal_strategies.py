        # Oversold bounce
        if k < 20:
            return Signal(
                action="buy",
                confidence=0.6,
                strategy=self.name,
                reason=f"Stochastic oversold ({k:.1f})",
                strength=(20 - k) / 20,
            )
        
        # Overbought reversal
        if k > 80:
            return Signal(
                action="sell",
                confidence=0.6,
                strategy=self.name,
                reason=f"Stochastic overbought ({k:.1f})",
                strength=(k - 80) / 20,
            )
        
        return None


