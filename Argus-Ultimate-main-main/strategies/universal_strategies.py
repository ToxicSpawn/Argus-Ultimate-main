class SMACrossover(TradingStrategy):
    """Simple Moving Average Crossover strategy."""
    
    def __init__(self, fast_period: int = 10, slow_period: int = 30):
        super().__init__(f"SMA_Cross_{fast_period}_{slow_period}")
        self.fast_period = fast_period
        self.slow_period = slow_period
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        if len(prices) < self.slow_period:
            return None
        
        fast_sma = np.mean(prices[-self.fast_period:])
        slow_sma = np.mean(prices[-self.slow_period:])
        
        prev_fast = np.mean(prices[-self.fast_period-1:-1])
        prev_slow = np.mean(prices[-self.slow_period-1:-1])
        
        # Bullish crossover
        if prev_fast <= prev_slow and fast_sma > slow_sma:
            strength = (fast_sma - slow_sma) / slow_sma
            return Signal(
                action="buy",
                confidence=min(0.8, 0.5 + strength * 10),
                strategy=self.name,
                reason=f"SMA bullish crossover: {self.fast_period} crossed above {self.slow_period}",
                strength=strength,
            )
        
        # Bearish crossover
        if prev_fast >= prev_slow and fast_sma < slow_sma:
            strength = (slow_sma - fast_sma) / slow_sma
            return Signal(
                action="sell",
                confidence=min(0.8, 0.5 + strength * 10),
                strategy=self.name,
                reason=f"SMA bearish crossover: {self.fast_period} crossed below {self.slow_period}",
                strength=strength,
            )
        
        return None


class EMACrossover(TradingStrategy):
    """Exponential Moving Average Crossover."""
    
    def __init__(self, fast_period: int = 12, slow_period: int = 26):
        super().__init__(f"EMA_Cross_{fast_period}_{slow_period}")
        self.fast_period = fast_period
        self.slow_period = slow_period
    
    def _ema(self, prices: List[float], period: int) -> float:
        """Calculate EMA."""
        multiplier = 2 / (period + 1)
        ema = prices[0]
        for price in prices[1:]:
            ema = (price - ema) * multiplier + ema
        return ema
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        if len(prices) < self.slow_period:
            return None
        
        fast_ema = self._ema(prices[-self.fast_period:], self.fast_period)
        slow_ema = self._ema(prices[-self.slow_period:], self.slow_period)
        
        prev_fast = self._ema(prices[-self.fast_period-1:-1], self.fast_period)
        prev_slow = self._ema(prices[-self.slow_period-1:-1], self.slow_period)
        
        if prev_fast <= prev_slow and fast_ema > slow_ema:
            return Signal(
                action="buy",
                confidence=0.65,
                strategy=self.name,
                reason="EMA bullish crossover",
                strength=abs(fast_ema - slow_ema) / slow_ema,
            )
        elif prev_fast >= prev_slow and fast_ema < slow_ema:
            return Signal(
                action="sell",
                confidence=0.65,
                strategy=self.name,
                reason="EMA bearish crossover",
                strength=abs(slow_ema - fast_ema) / slow_ema,
            )
        
        return None


class ADXTrendStrength(TradingStrategy):
    """ADX-based trend strength strategy."""
    
    def __init__(self, period: int = 14, threshold: float = 25.0):
        super().__init__(f"ADX_Trend_{period}")
        self.period = period
        self.threshold = threshold
    
    def _calc_adx(self, prices: List[float]) -> float:
        """Simplified ADX calculation."""
        if len(prices) < self.period + 1:
            return 0.0
        
        # Calculate true range (simplified)
        tr_values = []
        for i in range(1, len(prices)):
            tr = abs(prices[i] - prices[i-1])
            tr_values.append(tr)
        
        # Calculate directional movement
        up_moves = []
        down_moves = []
        for i in range(1, len(prices)):
            up = prices[i] - prices[i-1] if prices[i] > prices[i-1] else 0
            down = prices[i-1] - prices[i] if prices[i] < prices[i-1] else 0
            up_moves.append(up)
            down_moves.append(down)
        
        # Simplified ADX
        avg_tr = np.mean(tr_values[-self.period:])
        avg_up = np.mean(up_moves[-self.period:])
        avg_down = np.mean(down_moves[-self.period:])
        
        if avg_tr == 0:
            return 0.0
        
        plus_di = (avg_up / avg_tr) * 100
        minus_di = (avg_down / avg_tr) * 100
        
        di_sum = plus_di + minus_di
        if di_sum == 0:
            return 0.0
        
        dx = abs(plus_di - minus_di) / di_sum * 100
        return dx
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        adx = self._calc_adx(prices)
        
        if adx > self.threshold:
            # Strong trend - follow direction
            recent_return = (prices[-1] - prices[-5]) / prices[-5]
            
            if recent_return > 0:
                return Signal(
                    action="buy",
                    confidence=min(0.8, 0.5 + adx / 100),
                    strategy=self.name,
                    reason=f"Strong uptrend (ADX={adx:.1f})",
                    strength=adx / 100,
                )
            else:
                return Signal(
                    action="sell",
                    confidence=min(0.8, 0.5 + adx / 100),
                    strategy=self.name,
                    reason=f"Strong downtrend (ADX={adx:.1f})",
                    strength=adx / 100,
                )
        
        return None


