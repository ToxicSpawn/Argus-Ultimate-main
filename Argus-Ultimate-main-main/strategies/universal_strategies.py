class BollingerBandReversion(TradingStrategy):
    """Bollinger Bands mean reversion."""
    
    def __init__(self, period: int = 20, num_std: float = 2.0):
        super().__init__(f"Bollinger_{period}_{num_std}")
        self.period = period
        self.num_std = num_std
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        if len(prices) < self.period:
            return None
        
        # Calculate bands
        mean = np.mean(prices[-self.period:])
        std = np.std(prices[-self.period:])
        
        upper = mean + self.num_std * std
        lower = mean - self.num_std * std
        
        current = prices[-1]
        
        # Price below lower band - buy (expect reversion to mean)
        if current < lower:
            deviation = (mean - current) / std
            return Signal(
                action="buy",
                confidence=min(0.8, 0.5 + deviation * 0.15),
                strategy=self.name,
                reason=f"Price below lower Bollinger Band (z={deviation:.2f})",
                strength=deviation / self.num_std,
            )
        
        # Price above upper band - sell (expect reversion to mean)
        if current > upper:
            deviation = (current - mean) / std
            return Signal(
                action="sell",
                confidence=min(0.8, 0.5 + deviation * 0.15),
                strategy=self.name,
                reason=f"Price above upper Bollinger Band (z={deviation:.2f})",
                strength=deviation / self.num_std,
            )
        
        return None


