class VolumeProfileTrading(TradingStrategy):
    """Trade based on volume profile (POC, VA, etc)."""
    
    def __init__(self, lookback: int = 100, num_bins: int = 20):
        super().__init__(f"VolumeProfile")
        self.lookback = lookback
        self.num_bins = num_bins
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        volumes = kwargs.get("volumes", [])
        
        if not volumes or len(prices) < self.lookback:
            return None
        
        # Build volume profile
        price_range = max(prices[-self.lookback:]) - min(prices[-self.lookback:])
        if price_range == 0:
            return None
        
        bin_size = price_range / self.num_bins
        min_price = min(prices[-self.lookback:])
        
        profile = np.zeros(self.num_bins)
        
        for i, price in enumerate(prices[-self.lookback:]):
            vol = volumes[-self.lookback + i] if i < len(volumes) else 1
            bin_idx = min(int((price - min_price) / bin_size), self.num_bins - 1)
            profile[bin_idx] += vol
        
        # Find Point of Control (POC)
        poc_idx = np.argmax(profile)
        poc_price = min_price + (poc_idx + 0.5) * bin_size
        
        # Current price relative to POC
        current = prices[-1]
        poc_deviation = (current - poc_price) / poc_price
        
        # Value Area (70% of volume)
        total_volume = np.sum(profile)
        value_area_threshold = total_volume * 0.35
        
        # Find value area high/low
        sorted_bins = np.argsort(profile)[::-1]
        cumulative = 0
        va_bins = []
        
        for idx in sorted_bins:
            cumulative += profile[idx]
            va_bins.append(idx)
            if cumulative >= total_volume * 0.7:
                break
        
        va_high = max(va_bins) * bin_size + min_price + bin_size
        va_low = min(va_bins) * bin_size + min_price
        
        # Trade signals
        if current < va_low:
            # Below value area - buy
            return Signal(
                action="buy",
                confidence=0.65,
                strategy=self.name,
                reason=f"Below value area low (POC={poc_price:.2f})",
                strength=(poc_price - current) / poc_price,
            )
        elif current > va_high:
            # Above value area - sell
            return Signal(
                action="sell",
                confidence=0.65,
                strategy=self.name,
                reason=f"Above value area high (POC={poc_price:.2f})",
                strength=(current - poc_price) / poc_price,
            )
        
        return None


