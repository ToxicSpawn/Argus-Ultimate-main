class PairsTrading(TradingStrategy):
    """Trade spread between correlated pairs."""
    
    def __init__(self, entry_z: float = 2.0, exit_z: float = 0.0):
        super().__init__(f"PairsTrade")
        self.entry_z = entry_z
        self.exit_z = exit_z
        self.spread_history: Deque[float] = deque(maxlen=100)
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        prices2 = kwargs.get("prices2", [])
        
        if not prices2 or len(prices) < 20 or len(prices2) < 20:
            return None
        
        # Calculate spread
        spread = prices[-1] - prices2[-1]
        self.spread_history.append(spread)
        
        if len(self.spread_history) < 20:
            return None
        
        # Calculate z-score of spread
        mean_spread = np.mean(self.spread_history)
        std_spread = np.std(self.spread_history)
        
        if std_spread == 0:
            return None
        
        z_score = (spread - mean_spread) / std_spread
        
        # Spread too wide - expect convergence
        if z_score > self.entry_z:
            return Signal(
                action="sell",
                confidence=min(0.8, 0.5 + abs(z_score) / 10),
                strategy=self.name,
                reason=f"Spread wide (z={z_score:.2f})",
                strength=abs(z_score) / self.entry_z,
            )
        elif z_score < -self.entry_z:
            return Signal(
                action="buy",
                confidence=min(0.8, 0.5 + abs(z_score) / 10),
                strategy=self.name,
                reason=f"Spread wide negative (z={z_score:.2f})",
                strength=abs(z_score) / self.entry_z,
            )
        
        return None


class CointegrationReversion(TradingStrategy):
    """Trade cointegrated series reversion."""
    
    def __init__(self, period: int = 50, entry_z: float = 2.0):
        super().__init__(f"CointReversion")
        self.period = period
        self.entry_z = entry_z
        self.spread_history: Deque[float] = deque(maxlen=200)
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        prices2 = kwargs.get("prices2", [])
        
        if not prices2 or len(prices) < self.period or len(prices2) < self.period:
            return None
        
        # Calculate hedge ratio (simplified)
        returns1 = np.diff(np.log(prices[-self.period:]))
        returns2 = np.diff(np.log(prices2[-self.period:]))
        
        if np.std(returns1) == 0:
            return None
        
        hedge_ratio = np.cov(returns1, returns2)[0, 1] / np.var(returns1)
        
        # Calculate spread
        spread = prices[-1] - hedge_ratio * prices2[-1]
        self.spread_history.append(spread)
        
        if len(self.spread_history) < 30:
            return None
        
        # Z-score of spread
        mean_spread = np.mean(self.spread_history)
        std_spread = np.std(self.spread_history)
        
        if std_spread == 0:
            return None
        
        z_score = (spread - mean_spread) / std_spread
        
        if z_score > self.entry_z:
            return Signal(
                action="sell",
                confidence=0.7,
                strategy=self.name,
                reason=f"Cointegration spread wide (z={z_score:.2f})",
                strength=abs(z_score) / self.entry_z,
            )
        elif z_score < -self.entry_z:
            return Signal(
                action="buy",
                confidence=0.7,
                strategy=self.name,
                reason=f"Cointegration spread wide (z={z_score:.2f})",
                strength=abs(z_score) / self.entry_z,
            )
        
        return None


