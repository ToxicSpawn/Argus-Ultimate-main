class SpreadScalping(TradingStrategy):
    """Scalp small price movements."""
    
    def __init__(self, threshold: float = 0.001):
        super().__init__(f"SpreadScalp_{threshold}")
        self.threshold = threshold
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        if len(prices) < 5:
            return None
        
        # Quick micro-momentum
        short_momentum = (prices[-1] - prices[-3]) / prices[-3]
        micro_momentum = (prices[-3] - prices[-5]) / prices[-5]
        
        # Scalp in direction of micro-momentum
        if abs(micro_momentum) > self.threshold:
            if micro_momentum > 0 and short_momentum > 0:
                return Signal(
                    action="buy",
                    confidence=0.5,
                    strategy=self.name,
                    reason=f"Micro momentum scalp (mom={micro_momentum:.3%})",
                    strength=abs(micro_momentum) / 0.01,
                )
            elif micro_momentum < 0 and short_momentum < 0:
                return Signal(
                    action="sell",
                    confidence=0.5,
                    strategy=self.name,
                    reason=f"Micro momentum scalp (mom={micro_momentum:.3%})",
                    strength=abs(micro_momentum) / 0.01,
                )
        
        return None


class OrderFlowScalping(TradingStrategy):
    """Scalp based on order flow imbalances."""
    
    def __init__(self, imbalance_threshold: float = 1.5):
        super().__init__(f"OrderFlowScalp")
        self.imbalance_threshold = imbalance_threshold
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        bid_volumes = kwargs.get("bid_volumes", [])
        ask_volumes = kwargs.get("ask_volumes", [])
        
        if not bid_volumes or not ask_volumes:
            return None
        
        # Calculate order flow imbalance
        bid_sum = sum(bid_volumes[-5:]) if len(bid_volumes) >= 5 else sum(bid_volumes)
        ask_sum = sum(ask_volumes[-5:]) if len(ask_volumes) >= 5 else sum(ask_volumes)
        
        if ask_sum == 0:
            return None
        
        imbalance = bid_sum / ask_sum
        
        if imbalance > self.imbalance_threshold:
            return Signal(
                action="buy",
                confidence=min(0.7, 0.5 + (imbalance - 1) * 0.2),
                strategy=self.name,
                reason=f"Bid imbalance ({imbalance:.2f}x)",
                strength=imbalance / self.imbalance_threshold,
            )
        elif imbalance < 1 / self.imbalance_threshold:
            return Signal(
                action="sell",
                confidence=min(0.7, 0.5 + (1/imbalance - 1) * 0.2),
                strategy=self.name,
                reason=f"Ask imbalance ({1/imbalance:.2f}x)",
                strength=(1/imbalance) / self.imbalance_threshold,
            )
        
        return None


