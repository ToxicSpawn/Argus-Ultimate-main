class RSIMomentum(TradingStrategy):
    """RSI-based momentum strategy."""
    
    def __init__(self, period: int = 14, overbought: float = 70, oversold: float = 30):
        super().__init__(f"RSI_{period}")
        self.period = period
        self.overbought = overbought
        self.oversold = oversold
    
    def _calc_rsi(self, prices: List[float]) -> float:
        """Calculate RSI."""
        if len(prices) < self.period + 1:
            return 50.0
        
        deltas = np.diff(prices[-self.period-1:])
        gains = deltas[deltas > 0].sum()
        losses = abs(deltas[deltas < 0].sum())
        
        if losses == 0:
            return 100.0
        
        rs = gains / losses
        rsi = 100 - (100 / (1 + rs))
        return rsi
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        rsi = self._calc_rsi(prices)
        
        # Oversold bounce
        if rsi < self.oversold:
            return Signal(
                action="buy",
                confidence=min(0.8, 0.5 + (self.oversold - rsi) / 30),
                strategy=self.name,
                reason=f"RSI oversold ({rsi:.1f})",
                strength=(self.oversold - rsi) / 30,
            )
        
        # Overbought reversal
        if rsi > self.overbought:
            return Signal(
                action="sell",
                confidence=min(0.8, 0.5 + (rsi - self.overbought) / 30),
                strategy=self.name,
                reason=f"RSI overbought ({rsi:.1f})",
                strength=(rsi - self.overbought) / 30,
            )
        
        # Momentum continuation
        if 50 < rsi < 60:
            return Signal(
                action="buy",
                confidence=0.4,
                strategy=self.name,
                reason=f"RSI momentum ({rsi:.1f})",
                strength=(rsi - 50) / 50,
            )
        
        return None


class MACDMomentum(TradingStrategy):
    """MACD momentum strategy."""
    
    def __init__(self, fast: int = 12, slow: int = 26, signal: int = 9):
        super().__init__(f"MACD_{fast}_{slow}_{signal}")
        self.fast = fast
        self.slow = slow
        self.signal_period = signal
    
    def _ema(self, prices: List[float], period: int) -> List[float]:
        """Calculate EMA series."""
        multiplier = 2 / (period + 1)
        ema_values = [prices[0]]
        for price in prices[1:]:
            ema_values.append((price - ema_values[-1]) * multiplier + ema_values[-1])
        return ema_values
    
    def analyze(self, prices: List[float], **kwargs) -> Optional[Signal]:
        if len(prices) < self.slow + self.signal_period:
            return None
        
        # Calculate MACD
        fast_ema = self._ema(prices, self.fast)
        slow_ema = self._ema(prices, self.slow)
        
        macd_line = [f - s for f, s in zip(fast_ema, slow_ema)]
        signal_line = self._ema(macd_line, self.signal_period)
        
        # Current and previous values
        macd_current = macd_line[-1]
        signal_current = signal_line[-1]
        macd_prev = macd_line[-2]
        signal_prev = signal_line[-2]
        
        # Bullish crossover
        if macd_prev <= signal_prev and macd_current > signal_current:
            return Signal(
                action="buy",
                confidence=0.7,
                strategy=self.name,
                reason="MACD bullish crossover",
                strength=abs(macd_current - signal_current) / abs(signal_current) if signal_current != 0 else 0.5,
            )
        
        # Bearish crossover
        if macd_prev >= signal_prev and macd_current < signal_current:
            return Signal(
                action="sell",
                confidence=0.7,
                strategy=self.name,
                reason="MACD bearish crossover",
                strength=abs(signal_current - macd_current) / abs(signal_current) if signal_current != 0 else 0.5,
            )
        
        return None


