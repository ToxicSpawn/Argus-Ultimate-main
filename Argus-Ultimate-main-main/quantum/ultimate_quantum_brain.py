    def _grover_search(self, features: np.ndarray) -> Dict[str, Any]:
        """Simulate Grover's search."""
        # Search through strategy parameter space
        # Quadratic speedup: O(sqrt(N)) vs O(N)
        
        # Define search space
        directions = [-1, 0, 1]
        thresholds = [0.3, 0.4, 0.5, 0.6, 0.7]
        
        best_score = -1
        best_direction = 0
        best_threshold = 0.5
        
        # Grover iteration (simulated)
        for direction in directions:
            for threshold in thresholds:
                # Oracle function: is this a good parameter set?
                score = self._grover_oracle(features, direction, threshold)
                
                if score > best_score:
                    best_score = score
                    best_direction = direction
                    best_threshold = threshold
        
        return {
            "direction": best_direction,
            "threshold": best_threshold,
            "confidence": best_score,
        }
    
    def _grover_oracle(
        self, features: np.ndarray, direction: int, threshold: float
    ) -> float:
        """Grover oracle function."""
        # Evaluate parameter quality
        feature_mean = np.mean(features[:5])
        
        if direction > 0 and feature_mean > threshold:
            return 0.8
        elif direction < 0 and feature_mean < (1 - threshold):
            return 0.8
        elif direction == 0:
            return 0.5
        else:
            return 0.2
    
    def _boltzmann_sample(self, features: np.ndarray) -> Dict[str, Any]:
        """Simulate quantum Boltzmann machine sampling."""
        # Energy-based model
        # Lower energy = more probable state
        
        # Calculate energy for each state
        energies = {}
        for direction in [-1, 0, 1]:
            energy = self._calculate_energy(features, direction)
            energies[direction] = energy
        
        # Convert energies to probabilities (Boltzmann distribution)
        temperatures = 0.1
        exp_energies = {
            d: np.exp(-e / temperatures) for d, e in energies.items()
        }
        total = sum(exp_energies.values())
        probabilities = {d: e / total for d, e in exp_energies.items()}
        
        # Sample from distribution
        directions = list(probabilities.keys())
        probs = list(probabilities.values())
        sampled_direction = np.random.choice(directions, p=probs)
        
        return {
            "direction": int(sampled_direction),
            "confidence": probabilities[sampled_direction],
            "energies": energies,
        }
    
    def _calculate_energy(self, features: np.ndarray, direction: int) -> float:
        """Calculate energy for Boltzmann machine."""
        # Energy function
        feature_energy = np.sum(features ** 2) / len(features)
        direction_energy = -direction * np.mean(features[:5])
        
        return feature_energy + direction_energy
    
    def _combine_signals(
        self, symbol: str, signals: List[QuantumSignal]
    ) -> QuantumBrainOutput:
        """Combine all quantum signals into final decision."""
        if not signals:
            return QuantumBrainOutput(
                symbol=symbol,
                final_direction=0,
                final_confidence=0,
            )
        
        # Weighted voting
        weighted_votes = { -1: 0.0, 0: 0.0, 1: 0.0 }
        total_weight = 0.0
        quantum_advantages = []
        
        for signal in signals:
            weight = self.module_weights.get(signal.signal_type, 1.0)
            weighted_votes[signal.direction] += signal.confidence * weight
            total_weight += weight
            quantum_advantages.append(signal.quantum_advantage)
        
        # Normalize
        if total_weight > 0:
            for direction in weighted_votes:
                weighted_votes[direction] /= total_weight
        
        # Final decision
        if weighted_votes[1] > weighted_votes[-1] and weighted_votes[1] > weighted_votes[0]:
            final_direction = 1
            final_confidence = weighted_votes[1]
        elif weighted_votes[-1] > weighted_votes[1] and weighted_votes[-1] > weighted_votes[0]:
            final_direction = -1
            final_confidence = weighted_votes[-1]
        else:
            final_direction = 0
            final_confidence = weighted_votes[0]
        
        # Calculate consensus
        if signals:
            same_direction = sum(1 for s in signals if s.direction == final_direction)
            consensus = same_direction / len(signals)
        else:
            consensus = 0
        
        # Calculate total quantum advantage
        total_advantage = np.mean(quantum_advantages) if quantum_advantages else 1.0
        
        return QuantumBrainOutput(
            symbol=symbol,
            kernel_signal=next((s for s in signals if s.signal_type == QuantumSignalType.KERNEL_CLASSIFICATION), None),
            rl_signal=next((s for s in signals if s.signal_type == QuantumSignalType.RL_ACTION), None),
            portfolio_signal=next((s for s in signals if s.signal_type == QuantumSignalType.OPTIMAL_PORTFOLIO), None),
            entanglement_signal=next((s for s in signals if s.signal_type == QuantumSignalType.ENTANGLEMENT_ALPHA), None),
            amplitude_signal=next((s for s in signals if s.signal_type == QuantumSignalType.AMPLITUDE_ESTIMATE), None),
            grover_signal=next((s for s in signals if s.signal_type == QuantumSignalType.GROVER_SEARCH), None),
            boltzmann_signal=next((s for s in signals if s.signal_type == QuantumSignalType.BOLTZMANN_ENERGY), None),
            final_direction=final_direction,
            final_confidence=final_confidence,
            quantum_consensus=consensus,
            total_quantum_advantage=total_advantage,
            coherence_score=consensus * final_confidence,
            entanglement_strength=np.mean([s.confidence for s in signals if s.signal_type == QuantumSignalType.ENTANGLEMENT_ALPHA] or [0]),
        )
    
    def _calculate_kelly(self, output: QuantumBrainOutput) -> float:
        """Calculate Kelly fraction for optimal bet sizing."""
        # Kelly criterion: f = (p * b - q) / b
        # p = probability of win, b = odds, q = 1 - p
        
        if output.final_direction == 0:
            return 0.0
        
        p = output.final_confidence
        q = 1 - p
        b = 2.0  # Assume 2:1 reward/risk ratio
        
        kelly = (p * b - q) / b
        
        # Half-Kelly for safety
        return max(0, kelly * 0.5)
    
    def _calculate_position_size(self, output: QuantumBrainOutput) -> float:
        """Calculate position size multiplier."""
        base = output.kelly_fraction
        
        # Adjust by consensus
        consensus_mult = 0.5 + output.quantum_consensus * 0.5
        
        # Adjust by quantum advantage
        advantage_mult = min(output.total_quantum_advantage / 2.0, 1.5)
        
        return float(np.clip(base * consensus_mult * advantage_mult, 0, 2))
    
    def _calculate_risk_params(
        self, output: QuantumBrainOutput
    ) -> Tuple[float, float]:
        """Calculate stop loss and take profit."""
        # Base parameters
        base_sl = 0.03
        base_tp = 0.08
        
        # Adjust by confidence
        if output.final_confidence > 0.8:
            # High confidence - tighter stops, higher targets
            sl = base_sl * 0.8
            tp = base_tp * 1.3
        elif output.final_confidence > 0.6:
            sl = base_sl
            tp = base_tp
        else:
            # Low confidence - wider stops, lower targets
            sl = base_sl * 1.2
            tp = base_tp * 0.8
        
        # Adjust by consensus
        if output.quantum_consensus > 0.8:
            tp *= 1.2  # Higher targets when quantum modules agree
        
        return sl, tp
    
    def _calculate_rsi(self, prices: List[float], period: int = 14) -> float:
        """Calculate RSI."""
        if len(prices) < period + 1:
            return 50.0
        
        deltas = np.diff(prices[-(period + 1):])
        gains = np.where(deltas > 0, deltas, 0)
        losses = np.where(deltas < 0, -deltas, 0)
        
        avg_gain = np.mean(gains) if len(gains) > 0 else 0
        avg_loss = np.mean(losses) if len(losses) > 0 else 0
        
        if avg_loss == 0:
            return 100.0 if avg_gain > 0 else 50.0
        
        rs = avg_gain / avg_loss
        return 100 - (100 / (1 + rs))
    
